namespace IntergalacticControls
{
    /// <summary>
    /// Enumerates between different operation input types
    /// </summary>
    public enum InputType
    {
        /// <summary>
        /// Bool type
        /// </summary>
        Bool,

        /// <summary>
        /// Byte type
        /// </summary>
        Byte,

        /// <summary>
        /// Int type
        /// </summary>
        Int,

        /// <summary>
        /// Float type
        /// </summary>
        Float,

        /// <summary>
        /// Double type
        /// </summary>
        Double,

        /// <summary>
        /// Pixel type
        /// </summary>
        Color,

        /// <summary>
        /// Convolution mask type
        /// </summary>
        DoubleMask,

        /// <summary>
        /// ImageBase type
        /// </summary>
        Image,

        /// <summary>
        /// Array of doubles
        /// </summary>
        DoubleArray,

        /// <summary>
        /// Binary Mask type
        /// </summary>
        BinaryMask
    }
}
